    {
        Route::create([
            'user' => \Auth::id(),
            'driver' => $request->driver,
            'from' => $request->from,
            'destination' => $request->destination,
            'depart_time' => $request->depart_time,
            'adult_price' => $request->adult_price,
            'child_price' => $request->child_price,
        ]);
    }

